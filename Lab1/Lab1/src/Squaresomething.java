public class Squaresomething {
    static int square;
    static double squareD;
    static float squareF;

    public static int Squaresomething(int x) {
        square = x * x;
        return square;
    }

    public static double Squaresomething(double x) {
        squareD = x * x;
        return squareD;
    }

    public static float Squaresomething(float x) {
        squareF = x * x;
        return squareF;
    }
}
